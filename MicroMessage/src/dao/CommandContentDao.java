package dao;

import java.io.IOException;
import java.util.ArrayList;
import java.util.List;

import org.apache.ibatis.session.SqlSession;

import bean.Command;
import bean.CommandContent;
import bean.Message;
import db.DBAccess;

/*
 * 与指令表对应的数据库操作的类
 */
public class CommandContentDao {
	/*
	 * 根据查询条件查询指令列表
	 */
	public List<Command> queryCommandList(String name,String description){
		DBAccess dbAccess = new DBAccess();
		List<Command> commandList = new ArrayList<Command>();
		SqlSession sqlSession = null;
		try {
			sqlSession = dbAccess.getSqlSession();
			Command command = new Command();
			command.setName(name);
			command.setDescription(description);
			//通过sqlSession执行SQL语句
			commandList = sqlSession.selectList("Command.queryCommandList",command);
			
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} finally{
			if(sqlSession!=null)
			sqlSession.close();
		}
		return commandList;
	}
	
	/*
	 * 批量新增
	 */
	public void addBatch(List<CommandContent>commandContent){
		DBAccess dbAccess = new DBAccess();
		SqlSession sqlSession = null;
		try {
			sqlSession = dbAccess.getSqlSession();
			//通过sqlSession执行SQL语句
			ICommandContent icommandContent = sqlSession.getMapper(ICommandContent.class);
			icommandContent.addBatch(commandContent);
			sqlSession.commit();
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} finally{
			if(sqlSession!=null)
			sqlSession.close();
		}
	}
}
